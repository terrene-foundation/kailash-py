# ---------------------------------------------------------------------------
# run_id semantics
# ---------------------------------------------------------------------------


@pytest.mark.unit
def test_run_id_autogenerated_when_omitted() -> None:
    """`DLDiagnostics(model)` generates a UUID-shaped `run_id`."""
    model = nn.Linear(4, 2)
    diag = DLDiagnostics(model)
    assert isinstance(diag.run_id, str)
    # UUID4 hex = 32 chars of [0-9a-f].
    assert len(diag.run_id) == 32
    assert all(c in "0123456789abcdef" for c in diag.run_id)


@pytest.mark.unit
def test_run_id_unique_across_sessions() -> None:
    """Two separately-constructed sessions get distinct auto-generated IDs."""
    model = nn.Linear(4, 2)
    a = DLDiagnostics(model)
    b = DLDiagnostics(model)
    assert a.run_id != b.run_id


@pytest.mark.unit
def test_run_id_kwarg_overrides_default() -> None:
    """An explicit `run_id` kwarg is preserved verbatim."""
    model = nn.Linear(4, 2)
    diag = DLDiagnostics(model, run_id="explicit-run-id")
    assert diag.run_id == "explicit-run-id"


