@pytest.mark.unit
def test_lr_range_test_rejects_missing_loss_fn() -> None:
    """`lr_range_test` MUST NOT have a silent-default loss_fn."""
    model = nn.Linear(4, 2)
    with pytest.raises(ValueError, match="loss_fn is required"):
        DLDiagnostics.lr_range_test(model, dataloader=[])


@pytest.mark.unit
def test_lr_range_test_rejects_steps_below_two() -> None:
    """`lr_range_test` needs at least 2 steps for a meaningful sweep."""
    model = nn.Linear(4, 2)
    with pytest.raises(ValueError, match="steps must be >= 2"):
        DLDiagnostics.lr_range_test(model, dataloader=[], loss_fn=nn.MSELoss(), steps=1)


@pytest.mark.unit
def test_lr_range_test_rejects_inverted_lr_range() -> None:
    """`lr_max` MUST be strictly greater than `lr_min`."""
    model = nn.Linear(4, 2)
    with pytest.raises(ValueError, match="0 < lr_min < lr_max"):
        DLDiagnostics.lr_range_test(
            model, dataloader=[], loss_fn=nn.MSELoss(), lr_min=1.0, lr_max=0.1
        )
