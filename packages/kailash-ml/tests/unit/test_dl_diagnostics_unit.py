# ---------------------------------------------------------------------------
# report() + dataframe accessors — empty-state contract
# ---------------------------------------------------------------------------


@pytest.mark.unit
def test_report_on_empty_session_returns_unknown_findings() -> None:
    """A session with no `track_*()` calls still returns a well-formed dict."""
    model = nn.Linear(4, 2)
    diag = DLDiagnostics(model)
    report = diag.report()
    assert report["batches"] == 0
    assert report["epochs"] == 0
    assert report["gradient_flow"]["severity"] == "UNKNOWN"
    assert report["dead_neurons"]["severity"] == "UNKNOWN"
    assert report["loss_trend"]["severity"] == "UNKNOWN"
    assert report["run_id"] == diag.run_id


@pytest.mark.unit
def test_empty_dataframe_accessors_return_zero_height() -> None:
    """All `*_df()` accessors return empty polars DataFrames on a fresh session."""
    model = nn.Linear(4, 2)
    diag = DLDiagnostics(model)
    assert diag.batches_df().height == 0
    assert diag.epochs_df().height == 0
    assert diag.gradients_df().height == 0
    assert diag.activations_df().height == 0
    assert diag.dead_neurons_df().height == 0


