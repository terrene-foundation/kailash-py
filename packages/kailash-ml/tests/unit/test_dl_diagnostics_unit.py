# Plotly extras-gating — plot_*() raises ImportError naming [dl]
# ---------------------------------------------------------------------------
#
# Because plotly happens to be in base deps today, we simulate the
# missing-extra scenario by monkey-patching the lazy loader to raise.
# This verifies the loud-fail contract without uninstalling plotly.


@pytest.mark.unit
def test_plot_loss_curves_raises_loudly_when_plotly_missing(
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    """`plot_loss_curves` raises ImportError naming `[dl]` when plotly is absent."""
    model = nn.Linear(4, 2)
    diag = DLDiagnostics(model)

    def _no_plotly() -> None:
        raise ImportError(
            "Plotting methods require plotly. Install the deep-learning extras: "
            "pip install kailash-ml[dl]"
        )

    monkeypatch.setattr(dl_mod, "_require_plotly", _no_plotly)
    with pytest.raises(ImportError, match=r"kailash-ml\[dl\]"):
        diag.plot_loss_curves()


