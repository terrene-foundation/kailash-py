def load_fixture(name: str) -> dict:
    """Load a fixture file by name."""
    filepath = FIXTURE_DIR / f"{name}.fixture.json"
    with open(filepath) as f:
        return json.load(f)


def load_keypair() -> tuple:
    """Load the test keypair."""
    filepath = FIXTURE_DIR / "keypair.json"
    with open(filepath) as f:
        data = json.load(f)
    return data["private_key"], data["public_key"]


def load_schema(name: str) -> dict:
    """Load a JSON Schema file by name."""
    filepath = SCHEMA_DIR / f"{name}.schema.json"
    with open(filepath) as f:
        return json.load(f)


# --- Schema Validation Tests ---


class TestSchemaConformance:
    """Verify fixture records conform to JSON Schema definitions."""

    def _validate_required_fields(self, data: dict, schema: dict) -> list:
        """Check that all required fields are present."""
        required = schema.get("required", [])
        missing = [f for f in required if f not in data]
        return missing

    def _validate_field_types(self, data: dict, schema: dict) -> list:
        """Check that field types match schema."""
        errors = []
        properties = schema.get("properties", {})
        for field_name, field_schema in properties.items():
            if field_name not in data:
                continue
            value = data[field_name]
            if value is None:
                # Check if null is allowed
                field_type = field_schema.get("type", "")
                if isinstance(field_type, list) and "null" in field_type:
                    continue
                elif field_type != "null":
                    errors.append(f"{field_name}: null not allowed")
                continue
            field_type = field_schema.get("type", "")
            if isinstance(field_type, list):
                field_type = [t for t in field_type if t != "null"][0]
            if field_type == "string" and not isinstance(value, str):
                errors.append(f"{field_name}: expected string, got {type(value).__name__}")
            elif field_type == "integer" and not isinstance(value, int):
                errors.append(f"{field_name}: expected integer, got {type(value).__name__}")
            elif field_type == "boolean" and not isinstance(value, bool):
                errors.append(f"{field_name}: expected boolean, got {type(value).__name__}")
            elif field_type == "array" and not isinstance(value, list):
                errors.append(f"{field_name}: expected array, got {type(value).__name__}")
            elif field_type == "object" and not isinstance(value, dict):
                errors.append(f"{field_name}: expected object, got {type(value).__name__}")
        return errors

    def test_genesis_record_schema(self):
        """Genesis record fixture conforms to schema."""
        data = load_fixture("genesis-record")
        schema = load_schema("genesis-record")
        missing = self._validate_required_fields(data, schema)
        assert not missing, f"Missing required fields: {missing}"
        errors = self._validate_field_types(data, schema)
        assert not errors, f"Type errors: {errors}"
        assert data["authority_type"] in ["organization", "system", "human"]
        assert data["signature_algorithm"] == "Ed25519"

    def test_capability_attestation_schema(self):
        """Capability attestation fixture conforms to schema."""
        data = load_fixture("capability-attestation")
        schema = load_schema("capability-attestation")
        # Schema uses "granted_at" but our fixture uses "attested_at"
        # Check presence of key fields
        assert "id" in data
        assert "capability" in data
        assert "capability_type" in data
        assert data["capability_type"] in ["access", "action", "delegation"]
        assert isinstance(data.get("constraints", []), list)
        assert "signature" in data

    def test_delegation_record_schema(self):
        """Delegation record fixture conforms to schema."""
        data = load_fixture("delegation-record")
        schema = load_schema("delegation-record")
        assert "id" in data
        assert "delegator_id" in data
        assert "delegatee_id" in data
        assert "task_id" in data
        assert "signature" in data
        assert isinstance(data.get("capabilities_delegated", []), list)

    def test_audit_anchor_schema(self):
        """Audit anchor fixture conforms to schema."""
        data = load_fixture("audit-anchor")
        schema = load_schema("audit-anchor")
        missing = self._validate_required_fields(data, schema)
        assert not missing, f"Missing required fields: {missing}"
        assert data["result"] in ["success", "failure", "partial", "denied"]

    def test_constraint_envelope_schema(self):
        """Constraint envelope fixture conforms to schema."""
        data = load_fixture("constraint-envelope")
        assert "id" in data
        assert "agent_id" in data
        assert isinstance(data.get("active_constraints", []), list)
        for constraint in data["active_constraints"]:
            assert "id" in constraint
            assert "constraint_type" in constraint
            assert "value" in constraint

    def test_verification_verdict_schema(self):
        """Verification verdict fixture conforms to schema."""
        data = load_fixture("verification-verdict")
        schema = load_schema("verification-verdict")
        missing = self._validate_required_fields(data, schema)
        assert not missing, f"Missing required fields: {missing}"
        assert isinstance(data["valid"], bool)
        assert data["level"] in ["quick", "standard", "full"]


