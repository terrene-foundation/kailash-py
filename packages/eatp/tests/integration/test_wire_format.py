# --- Round-Trip Tests ---


class TestSerializationRoundTrip:
    """Verify records can be serialized and deserialized without loss."""

    def test_genesis_record_round_trip(self):
        """Genesis record round-trips through dict serialization."""
        priv_key, pub_key = generate_keypair()
        original = GenesisRecord(
            id="gen-test-roundtrip",
            agent_id="agent-rt",
            authority_id="org-rt",
            authority_type=AuthorityType.ORGANIZATION,
            created_at=datetime(2025, 1, 15, 10, 0, 0, tzinfo=timezone.utc),
            signature=sign({"test": "data"}, priv_key),
            metadata={"key": "value"},
        )
        # Serialize
        payload = original.to_signing_payload()
        # Verify all fields present
        assert payload["id"] == original.id
        assert payload["agent_id"] == original.agent_id
        assert payload["authority_type"] == "organization"

    def test_delegation_record_round_trip(self):
        """Delegation record round-trips through dict serialization."""
        priv_key, _ = generate_keypair()
        original = DelegationRecord(
            id="del-test-roundtrip",
            delegator_id="agent-001",
            delegatee_id="agent-002",
            task_id="task-001",
            capabilities_delegated=["read", "write"],
            constraint_subset=["max_100"],
            delegated_at=datetime(2025, 1, 15, 10, 0, 0, tzinfo=timezone.utc),
            signature=sign({"test": "data"}, priv_key),
            delegation_chain=["org", "agent-001", "agent-002"],
            delegation_depth=1,
        )
        # Serialize and deserialize
        data = original.to_dict()
        restored = DelegationRecord.from_dict(data)
        assert restored.id == original.id
        assert restored.delegator_id == original.delegator_id
        assert restored.delegatee_id == original.delegatee_id
        assert restored.task_id == original.task_id
        assert restored.capabilities_delegated == original.capabilities_delegated
        assert restored.delegation_chain == original.delegation_chain
        assert restored.delegation_depth == original.delegation_depth

    def test_audit_anchor_round_trip(self):
        """Audit anchor round-trips through dict serialization."""
