# --- Hash Determinism Tests ---


class TestHashDeterminism:
    """Verify that hashing is deterministic across serialization."""

    def test_hash_chain_deterministic(self):
        """Same input always produces same hash."""
        data = {"id": "test", "action": "verify", "timestamp": "2025-01-15T10:00:00Z"}
        hash1 = hash_chain(data)
        hash2 = hash_chain(data)
        assert hash1 == hash2

    def test_hash_chain_key_order_independent(self):
        """Hash is independent of key insertion order."""
        data1 = {"b": 2, "a": 1}
        data2 = {"a": 1, "b": 2}
        assert hash_chain(data1) == hash_chain(data2)

    def test_trust_chain_hash_deterministic(self):
        """TrustLineageChain hash is deterministic."""
        data = load_fixture("full-chain")
        chain1 = TrustLineageChain.from_dict(data)
        chain2 = TrustLineageChain.from_dict(data)
        assert chain1.hash() == chain2.hash()

    def test_different_chains_different_hashes(self):
        """Different chains produce different hashes."""
        priv_key, _ = generate_keypair()
        genesis1 = GenesisRecord(
            id="gen-001",
            agent_id="agent-001",
            authority_id="org",
            authority_type=AuthorityType.ORGANIZATION,
            created_at=datetime(2025, 1, 1, tzinfo=timezone.utc),
            signature=sign({"id": "gen-001"}, priv_key),
        )
        genesis2 = GenesisRecord(
            id="gen-002",
            agent_id="agent-002",
            authority_id="org",
            authority_type=AuthorityType.ORGANIZATION,
            created_at=datetime(2025, 1, 1, tzinfo=timezone.utc),
            signature=sign({"id": "gen-002"}, priv_key),
        )
        chain1 = TrustLineageChain(genesis=genesis1)
        chain2 = TrustLineageChain(genesis=genesis2)
        assert chain1.hash() != chain2.hash()


# --- Fixture Integrity Tests ---


class TestFixtureIntegrity:
    """Verify the fixture files themselves are valid."""

    def test_manifest_exists(self):
        """Manifest file exists and is valid JSON."""
        filepath = FIXTURE_DIR / "manifest.json"
        assert filepath.exists()
        with open(filepath) as f:
            manifest = json.load(f)
        assert manifest["generated_by"] == "python-eatp-sdk"
        assert "fixtures" in manifest

    def test_all_fixtures_exist(self):
        """All fixtures listed in manifest exist."""
        filepath = FIXTURE_DIR / "manifest.json"
        with open(filepath) as f:
            manifest = json.load(f)
        for name in manifest["fixtures"]:
            fixture_path = FIXTURE_DIR / f"{name}.fixture.json"
            assert fixture_path.exists(), f"Missing fixture: {name}"

    def test_all_fixtures_valid_json(self):
        """All fixture files parse as valid JSON."""
        for fixture_file in FIXTURE_DIR.glob("*.fixture.json"):
            with open(fixture_file) as f:
                data = json.load(f)
            assert isinstance(data, dict), f"{fixture_file.name} is not a dict"

    def test_keypair_fixture(self):
        """Keypair fixture contains valid keys."""
        priv_key, pub_key = load_keypair()
        assert len(priv_key) > 0
        assert len(pub_key) > 0
        # Verify keys work
        sig = sign("test", priv_key)
        assert verify_signature("test", sig, pub_key)

    def test_all_schemas_valid_json(self):
        """All schema files parse as valid JSON."""
        for schema_file in SCHEMA_DIR.glob("*.schema.json"):
            with open(schema_file) as f:
                data = json.load(f)
            assert "$schema" in data
            assert "type" in data
