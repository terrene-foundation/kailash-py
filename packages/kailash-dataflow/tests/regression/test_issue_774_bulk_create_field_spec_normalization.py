@pytest.mark.integration
async def test_bulk_create_round_trip_via_db_model_dict_form(pg_test_suite):
    """Canonical ``@db.model`` path produces dict-form fields and round-trips.

    Asserts the fix preserves the canonical happy path: ``@db.model``
    populates ``_model_fields`` in dict-form (engine.py:1858), the
    constructor normalizer is a no-op, and ``db.express.bulk_create``
    completes without error.

    Per ``rules/testing.md`` § "State Persistence Verification": every
    write is verified with a read-back.
    """
    from dataflow import DataFlow

    db_url = pg_test_suite.config.url
    table_suffix = f"{int(time.time() * 1_000_000)}_{uuid.uuid4().hex[:6]}"
    db = DataFlow(db_url, auto_migrate=True)

    @db.model
    class Issue774User:  # noqa: N801 — table name must include timestamp for isolation
        name: str
        email: str
        active: bool

    # Force unique table name to isolate between parallel test runs.
    Issue774User.__tablename__ = f"issue774_users_{table_suffix}"  # type: ignore[attr-defined]

    await db.initialize()
    try:
        records = [
            {
                "name": "Alice",
                "email": f"alice_{table_suffix}@example.com",
                "active": True,
            },
            {
                "name": "Bob",
                "email": f"bob_{table_suffix}@example.com",
                "active": False,
            },
        ]
        # The path that crashed pre-fix on bare-type input now flows
        # through the canonical dict-form path. Asserts the contract is
        # whole: validate → execute → result.
        result = await db.express.bulk_create("Issue774User", records)
        assert isinstance(result, (list, dict))

        # Read-back proves persistence (rules/testing.md §
        # "State Persistence Verification").
        rows = await db.express.list("Issue774User", filter={})
        assert len(rows) >= 2
        emails = {row["email"] for row in rows}
        assert f"alice_{table_suffix}@example.com" in emails
        assert f"bob_{table_suffix}@example.com" in emails
    finally:
        # Best-effort cleanup; teardown errors do NOT mask test failures.
        try:
            async with pg_test_suite.get_connection() as conn:
                await conn.execute(
                    f'DROP TABLE IF EXISTS "issue774_users_{table_suffix}" CASCADE'
                )
        except Exception:  # noqa: BLE001 — cleanup-path
            pass
