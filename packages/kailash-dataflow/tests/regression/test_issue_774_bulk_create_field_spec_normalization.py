"""Regression test for issue #774 — bulk_create AttributeError on bare-type field specs.

Before the fix, ``self.model_fields.get(field_name, {}).get("type")`` at
``dataflow/core/nodes.py:837`` raised
``AttributeError: type object 'str' has no attribute 'get'`` when callers
passed bare-type field-spec form (``{"name": str}``) to
``NodeGenerator.generate_bulk_nodes``. Canonical ``@db.model`` produces
dict-form (``{"name": {"type": str, "required": True}}``); the public
``NodeGenerator`` API accepted both shapes by signature but only the
dict-form by behavior.

Fix: ``_normalize_field_specs`` at the constructor boundary
(``DataFlowNode.__init__``) normalizes any caller-supplied shape to
canonical dict-form, and the module-level helpers (``_coerce_record_id``,
``convert_datetime_fields``) defensively accept both shapes for any
external caller bypassing the node constructor.

Test layers:

1. **Tier 1 (unit)** — replays the original failing test
   (``test_bulk_create_with_data_parameter``) via
   ``NodeGenerator.generate_bulk_nodes`` with bare-type fields and asserts
   ``validate_inputs`` no longer raises ``AttributeError``.
2. **Tier 1 (structural)** — pins the ``_normalize_field_specs`` contract
   so future refactors that change the normalization shape break loudly.
3. **Tier 2 (integration)** — exercises ``db.express.bulk_create`` against
   real PostgreSQL with a ``@db.model``-decorated class (dict-form path)
   to prove the canonical happy path is unaffected.

Per ``rules/testing.md`` § "Behavioral Regression Tests Over Source-Grep"
and § "End-to-End Pipeline Regression": the tests CALL the function and
assert the user-visible outcome, not the source-line state.
"""

from __future__ import annotations

import time
import uuid
from unittest.mock import MagicMock

import pytest

from dataflow.core.nodes import NodeGenerator, _normalize_field_specs

pytestmark = pytest.mark.regression


# ---------------------------------------------------------------------------
# Tier 1: Unit — bare-type field-spec form does not crash validate_inputs
# ---------------------------------------------------------------------------


def _build_bulk_node_with_bare_type_fields():
    """Build a BulkCreate node via the public NodeGenerator API with bare-type fields.

    Mirrors the failing test
    ``tests/unit/core/test_auto_generated_bulk_parameter_mapping.py``
    ``TestAutoGeneratedBulkParameterMapping::setup_method`` shape.
    """
    mock_dataflow = MagicMock()
    mock_dataflow.config.security.multi_tenant = False
    mock_dataflow._tenant_context = {}
    generator = NodeGenerator(mock_dataflow)

    # Bare-type form — the shape that crashed pre-fix.
    test_fields = {"name": str, "email": str, "active": bool}
    nodes = generator.generate_bulk_nodes("User", test_fields)
    return nodes["UserBulkCreateNode"]


def test_bulk_create_validate_inputs_does_not_crash_on_bare_type_fields():
    """Pre-fix: AttributeError: type object 'str' has no attribute 'get'.

    Post-fix: validate_inputs returns successfully because the constructor
    normalizes bare-type values into canonical dict-form.
    """
    bulk_create_node = _build_bulk_node_with_bare_type_fields()
    instance = bulk_create_node()

    test_data = [
        {"name": "Alice", "email": "alice@example.com"},
        {"name": "Bob", "email": "bob@example.com"},
    ]

    # The crash site: validate_inputs walks model_fields to enforce the
    # declared-string guard against dict / list / set / tuple values.
    # On bare-type input, the .get(name, {}).get("type") chain raised
    # AttributeError ('str' has no attribute 'get').
    validated = instance.validate_inputs(data=test_data)
    assert isinstance(
        validated, dict
    ), "validate_inputs must return a dict after normalization"


def test_bulk_create_run_returns_dict_with_bare_type_fields():
    """End-to-end mirror of the originally failing test (``data=`` kwarg).

    This test exercises ``.run(data=...)`` against the mock-DataFlow shape
    used by ``tests/unit/core/test_auto_generated_bulk_parameter_mapping.py``.
    The original test only asserted ``isinstance(result, dict)``; this
    regression test pins the same outcome for the bare-type field-spec
    branch so the bug class cannot recur silently.
    """
    bulk_create_node = _build_bulk_node_with_bare_type_fields()
    instance = bulk_create_node()

    test_data = [
        {"name": "Alice", "email": "alice@example.com"},
        {"name": "Bob", "email": "bob@example.com"},
    ]
    result = instance.run(data=test_data)
    assert isinstance(result, dict)


def test_normalize_field_specs_accepts_both_shapes():
    """Structural invariant: the normalizer accepts dict-form AND bare-type.

    If a future refactor changes the canonical shape, this test fails
    loudly and forces a re-audit of every model_fields call site.
    """
    # Bare-type input → dict-form output
    bare = {"name": str, "active": bool}
    out = _normalize_field_specs(bare)
    assert out == {
        "name": {"type": str, "required": True},
        "active": {"type": bool, "required": True},
    }

    # Already dict-form → preserved unchanged
    dict_form = {"id": {"type": int, "required": True, "primary_key": True}}
    assert _normalize_field_specs(dict_form) == dict_form

    # Mixed shape (defense-in-depth)
    mixed = {"id": {"type": int, "primary_key": True}, "name": str}
    assert _normalize_field_specs(mixed) == {
        "id": {"type": int, "primary_key": True},
        "name": {"type": str, "required": True},
    }

    # Empty / None safe
    assert _normalize_field_specs({}) == {}
    assert _normalize_field_specs(None) == {}  # type: ignore[arg-type]


