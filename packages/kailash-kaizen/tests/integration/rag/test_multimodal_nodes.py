class TestMultimodalRAGNodeGraphShape:
    """The wrapped sub-workflow's structural contract — node count, the
    parameter surface, and serialisation."""

    def test_create_workflow_wires_six_nodes(self):
        """The multimodal RAG sub-workflow wires exactly six nodes."""
        node = MultimodalRAGNode()
        workflow = node._create_workflow()  # type: ignore[attr-defined]
        assert len(workflow.nodes) == 6

    def test_enable_ocr_flag_threads_into_doc_preprocessor_codegen(self):
        """The enable_ocr constructor flag is interpolated into the
        doc_preprocessor codegen template (True vs False render distinctly)."""
        ocr_on = MultimodalRAGNode(enable_ocr=True)
        ocr_off = MultimodalRAGNode(enable_ocr=False)
        code_on = _node_code(
            ocr_on._create_workflow(), "doc_preprocessor"  # type: ignore[attr-defined]
        )
        code_off = _node_code(
            ocr_off._create_workflow(), "doc_preprocessor"  # type: ignore[attr-defined]
        )
        assert "if True and image_path" in code_on
        assert "if False and image_path" in code_off

    def test_image_encoder_flag_threads_into_encoder_codegen(self):
        """The image_encoder constructor value reaches the encoder template."""
        node = MultimodalRAGNode(image_encoder="blip-large")
        code = _node_code(
            node._create_workflow(), "multimodal_encoder"  # type: ignore[attr-defined]
        )
        assert "blip-large" in code

    def test_get_parameters_exposes_input_surface(self):
        """get_parameters returns the wrapped sub-workflow's input mapping."""
        params = MultimodalRAGNode().get_parameters()
        assert len(params) > 0

    def test_to_dict_round_trips_node_type(self):
        """to_dict serialises the node with its registered type name."""
        data = MultimodalRAGNode().to_dict()
        assert data["type"] == "MultimodalRAGNode"
