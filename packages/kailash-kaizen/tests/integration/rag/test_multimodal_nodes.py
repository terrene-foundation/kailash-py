"""Tier-2a integration coverage for ``kaizen.nodes.rag.multimodal``.

F8 shard B4. The 3 multimodal RAG nodes' real backend is the ``[rag]`` extra's
own ``Pillow`` and ``numpy`` — no container, no LLM key. These tests generate a
REAL small PNG with ``PIL.Image.new(...)`` in a fixture and feed real image
bytes / real file paths to the nodes; ``numpy`` backs the embedding-fallback
math. NO mocking (``@patch`` / ``MagicMock`` / ``unittest.mock`` are BLOCKED in
Tier 2 per the 3-tier testing rule). The dep IS the infra.

``MultimodalRAGNode`` is a ``WorkflowNode``; its ``_create_workflow()`` builds
a sub-workflow whose ``PythonCodeNode`` ``code=`` templates carry retrieval
logic. End-to-end runtime execution of that graph needs an LLM key (the
``query_analyzer`` / ``response_generator`` are ``LLMAgentNode`` steps) — a
documented scope boundary. This file covers what is exercisable with the
``[rag]`` extra alone: the codegen templates exercised DIRECTLY via ``exec``
against malformed input (the B1 codegen-regression pattern), plus the graph
shape and node parameter contracts.

Assertions are structural: result keys, score ranges/ordering, list lengths,
typed-error raises, real-PNG byte properties.
"""

