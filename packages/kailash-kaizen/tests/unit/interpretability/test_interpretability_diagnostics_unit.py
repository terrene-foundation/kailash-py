@pytest.mark.unit
def test_protocol_conformance_via_isinstance() -> None:
    """``isinstance(diag, Diagnostic)`` returns True at runtime.

    Load-bearing structural assertion — downstream consumers rely on
    the Protocol dispatch per ``rules/orphan-detection.md`` §1. If
    this fails, every telemetry sink that does
    ``record_diagnostic(d: Diagnostic)`` silently skips the adapter.
    """
    diag = InterpretabilityDiagnostics()
    assert isinstance(diag, Diagnostic)


@pytest.mark.unit
def test_context_manager_enter_returns_self() -> None:
    """``__enter__`` returns self (Protocol contract §2.2 item 2)."""
    diag = InterpretabilityDiagnostics()
    entered = diag.__enter__()
    assert entered is diag
    diag.__exit__(None, None, None)


@pytest.mark.unit
def test_report_callable_on_empty_session() -> None:
    """``report()`` is callable on an empty session and never raises.

    Protocol contract §2.2 item 4 — callable at any time, returns a
    dict with the expected keys even when no method has been invoked.
    """
    diag = InterpretabilityDiagnostics()
    report = diag.report()
    assert isinstance(report, dict)
    assert report["run_id"] == diag.run_id
    assert report["attention_heatmaps"] == 0
    assert report["logit_lens_sweeps"] == 0
    assert report["linear_probes"] == {"count": 0}
    assert report["sae_feature_reads"] == 0
    assert isinstance(report["messages"], list)


