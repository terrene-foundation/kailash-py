def _redis_available() -> bool:
    try:
        import redis  # noqa: F401
    except ImportError:
        return False
    return (
        os.environ.get("KAILASH_EVENTBUS_REDIS_URL") is not None
        or os.environ.get("REDIS_URL") is not None
    )


def test_redis_backend_importable_behind_optional_extra():
    """The broker backend class is importable from the public surface
    even when no Redis server is running (implementation completeness)."""
    assert RedisStreamsEventBackend is not None
    # Constructing requires the redis package (declared optional extra).
    import importlib.util

    if importlib.util.find_spec("redis") is None:
        with pytest.raises(ImportError, match="requires the 'redis' extra"):
            RedisStreamsEventBackend()
    else:
        # redis pkg present: construction must not raise (no connection yet)
        be = RedisStreamsEventBackend(url="redis://localhost:6379/0")
        assert isinstance(be, EventBackend)


@pytest.mark.requires_redis
async def test_redis_backend_roundtrip():
    """Tier 2 broker round-trip — xfail if no live Redis is reachable."""
    import importlib.util

    if importlib.util.find_spec("redis") is None:
        pytest.xfail("redis package not installed (optional extra)")

    url = (
        os.environ.get("KAILASH_EVENTBUS_REDIS_URL")
        or os.environ.get("REDIS_URL")
        or "redis://localhost:6379/0"
    )
    try:
        import redis.asyncio as redis_async

        client = redis_async.from_url(url)
        await client.ping()
        await client.aclose()
    except Exception:
        pytest.xfail(
            "no live Redis reachable; broker backend implementation is "
            "complete and importable but cannot be exercised without infra"
        )

    bus = EventBus(backend="redis", redis_url=url)
    received: list = []

    async def handler(payload):
        received.append(payload)

    sub = bus.subscribe("redis.evt", handler)
    await asyncio.sleep(0.2)  # let consumer task spin up
    await bus.publish("redis.evt", {"r": 1}, correlation_id="rt-1")
    # poll up to ~3s for the streamed event
    for _ in range(30):
        if received:
            break
        await asyncio.sleep(0.1)

    assert received == [{"r": 1}]
    await sub.unsubscribe()
    await bus.close()
