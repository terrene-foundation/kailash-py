async def test_publish_subscribe_roundtrip_original_payload():
    """Tier 2: publish -> subscribe round-trip asserts the handler is
    invoked with the ORIGINAL payload object content."""
    bus = EventBus()  # default in-memory, no external dependency
    assert bus.backend_name == "InMemoryEventBackend"

    received: list[dict] = []

    async def handler(payload):
        received.append(payload)

    sub = bus.subscribe("order.created", handler)
    assert isinstance(sub, Subscription)
    assert sub.active is True

    original = {"order_id": "o-99", "total": 42.5, "items": ["a", "b"]}
    await bus.publish("order.created", original)
    await asyncio.sleep(0.02)

    assert len(received) == 1
    assert received[0] == original
    assert received[0]["items"] == ["a", "b"]

    await sub.unsubscribe()
    assert sub.active is False
    # unsubscribe is idempotent
    await sub.unsubscribe()

    await bus.publish("order.created", {"order_id": "o-100"})
    await asyncio.sleep(0.02)
    assert len(received) == 1  # unchanged after unsubscribe

    await bus.close()


async def test_correlation_id_roundtrips_to_subscriber():
    """correlation_id propagates to the subscriber via subscribe_events."""
    bus = EventBus()
    seen: list[DomainEvent] = []

    async def handler(event: DomainEvent):
        seen.append(event)

    sub = bus.subscribe_events("trace.evt", handler)
    await bus.publish(
        "trace.evt", {"k": "v"}, correlation_id="trace-xyz", actor="svc-a"
    )
    await asyncio.sleep(0.02)

    assert len(seen) == 1
    assert seen[0].correlation_id == "trace-xyz"
    assert seen[0].actor == "svc-a"
    assert seen[0].payload == {"k": "v"}
    assert seen[0].event_type == "trace.evt"

    await sub.unsubscribe()
    await bus.close()


async def test_correlation_id_autogenerated_when_omitted():
    bus = EventBus()
    seen: list[DomainEvent] = []
    sub = bus.subscribe_events("x.evt", lambda e: seen.append(e) or _noop())
    await bus.publish("x.evt", {"a": 1})
    await asyncio.sleep(0.02)
    assert len(seen) == 1
    assert isinstance(seen[0].correlation_id, str)
    assert len(seen[0].correlation_id) >= 8  # uuid4
    await sub.unsubscribe()
    await bus.close()


async def _noop():
    return None


async def test_multiple_subscribers_same_event_type():
    bus = EventBus()
    a: list = []
    b: list = []
    sa = bus.subscribe("evt", lambda p: a.append(p) or _noop())
    sb = bus.subscribe("evt", lambda p: b.append(p) or _noop())
    await bus.publish("evt", {"n": 1})
    await asyncio.sleep(0.02)
    assert a == [{"n": 1}] and b == [{"n": 1}]
    await sa.unsubscribe()
    await bus.publish("evt", {"n": 2})
    await asyncio.sleep(0.02)
    assert a == [{"n": 1}]  # sa removed
    assert b == [{"n": 1}, {"n": 2}]  # sb still active
    await sb.unsubscribe()
    await bus.close()


async def test_handler_exception_does_not_break_other_handlers():
    bus = EventBus()
    good: list = []

    async def bad(_):
        raise RuntimeError("boom")

    async def good_h(p):
        good.append(p)

    s1 = bus.subscribe("evt", bad)
    s2 = bus.subscribe("evt", good_h)
    await bus.publish("evt", {"ok": True})
    await asyncio.sleep(0.02)
    assert good == [{"ok": True}]  # bad handler did not block good handler
    await s1.unsubscribe()
    await s2.unsubscribe()
    await bus.close()


