async def test_workflow_step_publishes_via_eventbus():
    """Tier 2: a WorkflowBuilder step publishes an event through the bus,
    a subscriber receives the original payload + correlation_id."""
    from kailash.runtime.local import LocalRuntime
    from kailash.workflow.builder import WorkflowBuilder

    bus = EventBus()
    received: list[DomainEvent] = []

    async def handler(event: DomainEvent):
        received.append(event)

    sub = bus.subscribe_events("workflow.order.created", handler)

    wf = WorkflowBuilder()
    wf.add_node(
        "EventPublishNode",
        "emit",
        {
            "event_bus": bus,
            "event_type": "workflow.order.created",
            "payload": {"order_id": "wf-1"},
            "correlation_id": "wf-trace-1",
        },
    )
    with LocalRuntime() as runtime:
        results, _run_id = runtime.execute(wf.build())

    await asyncio.sleep(0.05)

    assert results["emit"]["published"] is True
    assert results["emit"]["event_type_out"] == "workflow.order.created"
    assert results["emit"]["correlation_id_out"] == "wf-trace-1"
    assert len(received) == 1
    assert received[0].payload == {"order_id": "wf-1"}
    assert received[0].correlation_id == "wf-trace-1"

    await sub.unsubscribe()
    await bus.close()


