def test_eventbus_is_public_surface():
    """from kailash import EventBus works AND dir(kailash) exposes it."""
    assert hasattr(kailash, "EventBus")
    assert hasattr(kailash, "Subscription")
    assert hasattr(kailash, "DomainEvent")
    assert "EventBus" in dir(kailash)
    assert "EventBus" in kailash.__all__
    assert "Subscription" in kailash.__all__
    assert "DomainEvent" in kailash.__all__


def test_eventbus_required_surface_shape():
    """The minimum surface from issue #1054 exists with the right shape."""
    import inspect

    pub = inspect.signature(EventBus.publish)
    assert "event_type" in pub.parameters
    assert "payload" in pub.parameters
    assert "correlation_id" in pub.parameters
    assert pub.parameters["correlation_id"].kind == inspect.Parameter.KEYWORD_ONLY
    assert inspect.iscoroutinefunction(EventBus.publish)

    sub = inspect.signature(EventBus.subscribe)
    assert "event_type" in sub.parameters
    assert "handler" in sub.parameters
    assert not inspect.iscoroutinefunction(EventBus.subscribe)

    assert inspect.iscoroutinefunction(Subscription.unsubscribe)


