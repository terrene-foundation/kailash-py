// A2A Demo Frontend Application

class A2ADemo {
    constructor() {
        this.ws = null;
        this.agents = [];
        this.messages = [];
        this.insights = [];
        this.activeTask = null;
        
        this.initializeWebSocket();
        this.bindEvents();
        this.loadAgents();
    }
    
    initializeWebSocket() {
        this.ws = new WebSocket('ws://localhost:8080/ws');
        
        this.ws.onopen = () => {
            console.log('WebSocket connected');
            this.updateConnectionStatus(true);
        };
        
        this.ws.onmessage = (event) => {
            const data = JSON.parse(event.data);
            this.handleWebSocketMessage(data);
        };
        
        this.ws.onclose = () => {
            console.log('WebSocket disconnected');
            this.updateConnectionStatus(false);
            // Attempt reconnection after 3 seconds
            setTimeout(() => this.initializeWebSocket(), 3000);
        };
        
        this.ws.onerror = (error) => {
            console.error('WebSocket error:', error);
        };
    }
    
    handleWebSocketMessage(data) {
        switch (data.type) {
            case 'agent_message':
                this.handleAgentMessage(data.data);
                break;
            case 'agent_active':
                this.handleAgentActive(data.data);
                break;
            case 'task_created':
                this.handleTaskCreated(data.data);
                break;
            case 'task_completed':
                this.handleTaskCompleted(data.data);
                break;
            default:
                console.log('Unknown message type:', data.type);
        }
    }
    
    handleAgentMessage(message) {
        this.messages.push(message);
        this.renderMessage(message);
        
        // Update insights if it's an insight message
        if (message.message_type === 'insight') {
            this.insights.push({
                agent_name: message.agent_name,
                content: message.content,
                timestamp: message.timestamp
            });
            this.updateInsightsTab();
        }
        
        // Update memory stats if it's a memory operation
        if (message.message_type === 'memory_write' || message.message_type === 'memory_read') {
            this.updateMemoryStats();
        }
    }
    
    handleAgentActive(data) {
        // Highlight active agent
        document.querySelectorAll('.agent-card').forEach(card => {
            card.classList.remove('active');
        });
        
        const activeCard = document.querySelector(`[data-agent-id="${data.agent_id}"]`);
        if (activeCard) {
            activeCard.classList.add('active');
        }
        
        // Update activity status
        const status = document.getElementById('activityStatus');
        status.textContent = `Phase ${data.phase}/${data.total_phases}: ${data.agent_name} is working...`;
    }
    
    handleTaskCreated(data) {
        this.activeTask = data;
        const status = document.getElementById('activityStatus');
        status.textContent = `Research task created: ${data.topic}`;
    }
    
    handleTaskCompleted(data) {
        const status = document.getElementById('activityStatus');
        status.textContent = `Task completed! ${data.insights_generated} insights generated by ${data.agents_participated} agents`;
        
        // Remove active state from all agents
        document.querySelectorAll('.agent-card').forEach(card => {
            card.classList.remove('active');
        });
    }
    
    bindEvents() {
        // Start button
        document.getElementById('startBtn').addEventListener('click', () => {
            this.startResearchTask();
        });
        
        // Enter key in input
        document.getElementById('queryInput').addEventListener('keypress', (e) => {
            if (e.key === 'Enter') {
                this.startResearchTask();
            }
        });
        
        // Tab switching
        document.querySelectorAll('.tab-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                this.switchTab(e.target.dataset.tab);
            });
        });
        
        // Select all checkbox
        document.getElementById('selectAll').addEventListener('change', (e) => {
            document.querySelectorAll('.agent-checkbox').forEach(checkbox => {
                checkbox.checked = e.target.checked;
            });
        });
    }
    
    async loadAgents() {
        try {
            const response = await fetch('/api/agents');
            const data = await response.json();
            this.agents = data.agents;
            this.renderAgents();
            this.renderAgentCheckboxes();
        } catch (error) {
            console.error('Failed to load agents:', error);
        }
    }
    
    renderAgents() {
        const agentsList = document.getElementById('agentsList');
        agentsList.innerHTML = '';
        
        this.agents.forEach(agent => {
            const agentCard = document.createElement('div');
            agentCard.className = 'agent-card';
            agentCard.dataset.agentId = agent.id;
            
            agentCard.innerHTML = `
                <div class="agent-name">${agent.name}</div>
                <div class="agent-type">${agent.type} (${agent.style})</div>
                <div class="agent-capabilities">
                    ${agent.capabilities.map(cap => 
                        `<span class="capability-tag">${cap}</span>`
                    ).join('')}
                </div>
            `;
            
            agentsList.appendChild(agentCard);
        });
    }
    
    renderAgentCheckboxes() {
        const container = document.getElementById('agentCheckboxes');
        container.innerHTML = '';
        
        this.agents.forEach(agent => {
            const label = document.createElement('label');
            label.innerHTML = `
                <input type="checkbox" class="agent-checkbox" value="${agent.id}" checked>
                ${agent.name}
            `;
            container.appendChild(label);
        });
    }
    
    renderMessage(message) {
        const messageStream = document.getElementById('messageStream');
        
        // Clear welcome message if exists
        const welcomeMsg = messageStream.querySelector('.welcome-message');
        if (welcomeMsg) {
            welcomeMsg.remove();
        }
        
        const messageEl = document.createElement('div');
        messageEl.className = 'message';
        
        const timestamp = new Date(message.timestamp * 1000).toLocaleTimeString();
        
        let metadataHtml = '';
        if (message.metadata && Object.keys(message.metadata).length > 0) {
            metadataHtml = `
                <div class="message-metadata">
                    ${Object.entries(message.metadata).map(([key, value]) => 
                        `<span>${key}: ${value}</span>`
                    ).join('')}
                </div>
            `;
        }
        
        messageEl.innerHTML = `
            <div class="message-header">
                <span class="message-agent">${message.agent_name}</span>
                <span class="message-type ${message.message_type}">${this.formatMessageType(message.message_type)}</span>
            </div>
            <div class="message-content">${this.formatContent(message.content)}</div>
            ${metadataHtml}
            <div class="message-metadata">
                <span>${timestamp}</span>
            </div>
        `;
        
        messageStream.appendChild(messageEl);
        messageStream.scrollTop = messageStream.scrollHeight;
    }
    
    formatMessageType(type) {
        const typeMap = {
            'thinking': '🤔 Thinking',
            'insight': '💡 Insight',
            'memory_read': '📖 Reading Memory',
            'memory_write': '💾 Sharing Insight',
            'error': '❌ Error'
        };
        return typeMap[type] || type;
    }
    
    formatContent(content) {
        // Convert markdown-like formatting
        return content
            .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
            .replace(/\n/g, '<br>')
            .replace(/```(.*?)```/gs, '<pre>$1</pre>');
    }
    
    async startResearchTask() {
        const queryInput = document.getElementById('queryInput');
        const topic = queryInput.value.trim();
        
        if (!topic) {
            alert('Please enter a research topic');
            return;
        }
        
        // Get selected agents
        const selectedAgents = Array.from(document.querySelectorAll('.agent-checkbox:checked'))
            .map(cb => cb.value);
        
        if (selectedAgents.length === 0) {
            alert('Please select at least one agent');
            return;
        }
        
        // Disable button during execution
        const startBtn = document.getElementById('startBtn');
        startBtn.disabled = true;
        startBtn.textContent = 'Research in Progress...';
        
        // Clear previous messages
        this.messages = [];
        this.insights = [];
        document.getElementById('messageStream').innerHTML = '';
        
        try {
            const response = await fetch('/api/task/create', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify({
                    topic: topic,
                    task_type: 'research',
                    agents_to_use: selectedAgents
                })
            });
            
            const result = await response.json();
            console.log('Task created:', result);
            
        } catch (error) {
            console.error('Failed to create task:', error);
            alert('Failed to start research task. Please check the console.');
        } finally {
            // Re-enable button
            startBtn.disabled = false;
            startBtn.textContent = 'Start Research';
        }
    }
    
    switchTab(tabName) {
        // Update tab buttons
        document.querySelectorAll('.tab-btn').forEach(btn => {
            btn.classList.remove('active');
        });
        document.querySelector(`[data-tab="${tabName}"]`).classList.add('active');
        
        // Update tab content
        document.querySelectorAll('.tab-pane').forEach(pane => {
            pane.classList.remove('active');
        });
        document.getElementById(`${tabName}Tab`).classList.add('active');
        
        // Load tab-specific content
        if (tabName === 'memory') {
            this.updateMemoryStats();
            this.loadMemoryContent();
        } else if (tabName === 'insights') {
            this.updateInsightsTab();
        }
    }
    
    async updateMemoryStats() {
        try {
            const response = await fetch('/api/memory/stats');
            const stats = await response.json();
            
            document.getElementById('totalMemories').textContent = stats.memory_id_counter || 0;
            document.getElementById('activeSegments').textContent = 
                Object.keys(stats.segment_sizes || {}).length;
        } catch (error) {
            console.error('Failed to load memory stats:', error);
        }
    }
    
    async loadMemoryContent() {
        try {
            const response = await fetch('/api/insights/recent?limit=20');
            const data = await response.json();
            
            const memoryContent = document.getElementById('memoryContent');
            memoryContent.innerHTML = '';
            
            if (data.insights.length === 0) {
                memoryContent.innerHTML = '<p style="text-align: center; color: #999;">No memories yet. Start a research task to see shared insights.</p>';
                return;
            }
            
            data.insights.forEach(memory => {
                const memoryItem = document.createElement('div');
                memoryItem.className = 'memory-item';
                
                const importance = (memory.importance * 100).toFixed(0);
                
                memoryItem.innerHTML = `
                    <div style="font-weight: 600; margin-bottom: 5px;">
                        ${memory.agent_id} (Importance: ${importance}%)
                    </div>
                    <div style="margin-bottom: 10px;">${memory.content}</div>
                    <div class="memory-tags">
                        ${(memory.tags || []).map(tag => 
                            `<span class="memory-tag">${tag}</span>`
                        ).join('')}
                    </div>
                `;
                
                memoryContent.appendChild(memoryItem);
            });
        } catch (error) {
            console.error('Failed to load memory content:', error);
        }
    }
    
    updateInsightsTab() {
        const insightsList = document.getElementById('insightsList');
        
        if (this.insights.length === 0) {
            insightsList.innerHTML = '<p style="text-align: center; color: #999;">No insights generated yet. Insights will appear here as agents collaborate.</p>';
            return;
        }
        
        insightsList.innerHTML = '';
        
        // Show most recent insights first
        const recentInsights = [...this.insights].reverse().slice(0, 10);
        
        recentInsights.forEach(insight => {
            const insightItem = document.createElement('div');
            insightItem.className = 'insight-item';
            
            const timestamp = new Date(insight.timestamp * 1000).toLocaleTimeString();
            
            insightItem.innerHTML = `
                <div class="insight-content">${this.formatContent(insight.content)}</div>
                <div class="insight-meta">
                    <span>${insight.agent_name}</span>
                    <span>${timestamp}</span>
                </div>
            `;
            
            insightsList.appendChild(insightItem);
        });
    }
    
    updateConnectionStatus(connected) {
        const statusEl = document.getElementById('connectionStatus');
        const statusDot = statusEl.querySelector('.status-dot');
        
        if (connected) {
            statusEl.innerHTML = '<span class="status-dot connected"></span> Connected';
        } else {
            statusEl.innerHTML = '<span class="status-dot"></span> Disconnected';
        }
    }
}

// Initialize the app when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    new A2ADemo();
});